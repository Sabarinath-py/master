# comparison operators( ==, !=, <, >, <=, >=)
    # result will be a boolean value(True or False)

num1 = 10
num2 = 20

print(num1 > num2)      #10>20
print(num1 < num2)      #10<20
print(num1 >= num2)     #10>=20
print(num1 <= num2)     #10<=20
print(num1 != num2)     #10!=20
print(num1 == num2)     #10==20