# 6. Assignment operators (=, +=, -=, *=, /=, %=, //=, **=)

# 7. Bitwise operators (&, |, ^, ~, <<, >>)



# -> Arithmetic operators can be only used on numerical values (int, float)

    # num1                num2         operator         result

    # 200                 100             +               300
    # 200                 100             -               100
    # 10                  5               *               50
    # 10                  5               /               2.0
    # 123                10               %               3
    # 10                  3               //              3
    # 2                   4               **              16


# -> Comparison operators can be used on numerical and string values





# -> Logical operators can be used on boolean values





# -> Identity operators can be used on any object





